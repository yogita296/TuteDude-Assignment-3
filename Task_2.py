# Using the Math Module for Calculations

import math
a=input('Enter a number: ')
a=int(a)
print('Square root: ',math.sqrt(a))  #Square root of the number
print('Logarithm: ',math.log(a))   #Natural logarithm (log base e) of the number
print('Sine: ',math.sin(a))   #Sine of the number (in radians)
